    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
        if(txtNome.getText().equals("") || txtPreco.getText().equals("") || txtQuantidade.getText().equals("")){
            
            JOptionPane.showMessageDialog(this, "Um dos campos está vazio");
            
        }else try{
            
            ArrayList<Produto> produtos = pv.Listar();
            
            DefaultTableModel tblModel = (DefaultTableModel)tabela.getModel(); 
            
            Produto p1 = new Produto(produtos.size(),txtNome.getText(),Double.parseDouble(txtPreco.getText()), Integer.parseInt(txtQuantidade.getText()));
            
            
            pv.Adicionar(p1);
            JOptionPane.showMessageDialog(null, "Produto adicionado com sucesso");
            txtNome.setText("");
            txtQuantidade.setText("");
            txtPreco.setText("");
            
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Erro ao adicionar produto");
        }
    }//GEN-LAST:event_btnAdicionarActionPerformed

    private void btnListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListarActionPerformed
        
        DefaultTableModel tblModel = (DefaultTableModel)tabela.getModel();
        
        ArrayList<Produto> produtos = pv.Listar();
        
        for (int i = 0; i < 50; i++) {
            tblModel.setValueAt("", i, 0);
            tblModel.setValueAt("", i, 1);
            tblModel.setValueAt("", i, 2);
            tblModel.setValueAt("", i, 3);
        }
        
        for (int i = 0; i < produtos.size(); i++) {
            tblModel.setValueAt(produtos.get(i).getId(), i, 0);
            tblModel.setValueAt(produtos.get(i).getNome(), i, 1);
            tblModel.setValueAt(produtos.get(i).getPreco(), i, 2);
            tblModel.setValueAt(produtos.get(i).getQuantidade(), i, 3);
            
        }
        
    }//GEN-LAST:event_btnListarActionPerformed

