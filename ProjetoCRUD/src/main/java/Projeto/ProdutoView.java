    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        DefaultTableModel tblModel = (DefaultTableModel)tabela.getModel();
        
        ArrayList<Produto> produtos = pv.Listar();
                
        int id = (int) tblModel.getValueAt(tabela.getSelectedRow(), 0);
        
        if(pv.Remover(id)){
            JOptionPane.showMessageDialog(null, "Produto deletado com sucesso");
        }else{
            JOptionPane.showMessageDialog(null, "Erro ao deletar Produto");
        }
        
    }//GEN-LAST:event_btnRemoverActionPerformed

    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        DefaultTableModel tblModel = (DefaultTableModel)tabela.getModel();
        
        ArrayList<Produto> produtos = pv.Listar();
                
        int id = (int) tblModel.getValueAt(tabela.getSelectedRow(), 0);
        
        Produto p1 = new Produto(id,txtNome.getText(),Double.parseDouble(txtPreco.getText()), Integer.parseInt(txtQuantidade.getText()));
        
        if(pv.Atualizar(id, p1)){
            JOptionPane.showMessageDialog(null, "Produto atualizado com sucesso");
        }else{
            JOptionPane.showMessageDialog(null, "Erro ao atualizar o Produto");
        }
    }//GEN-LAST:event_btnAtualizarActionPerformed

